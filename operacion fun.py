def funcion_suma(x, y):
    resultado = x + y
    return resultado


num1 = float(input("Digite un valor"))
num2 = float(input("Digite otro valor"))
print(funcion_suma(num1, num2))

def operaciones(x, y):
    resultado = x - y
    return resultado
def multiplicacion(x,y):
    resultado = x * y
    return resultado
def division(x,y):
    resultado = x / y
    return resultado
print(operaciones(num1, num2))
print(multiplicacion(num1, num2))
print(division(num1, num2))